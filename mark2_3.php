<h3 class='album_title_header'>Battle Rages On - <em>1992</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/cover_battle_rages_on.png" alt="Battle Rages On Cover"/>
Is the third time the charm?  Well, not exactly although anything to get the taste of 'Slaves and Masters' off the palette is welcome.  This is the second time the MK II lineup reunites after breaking up. Evidently Ritchie wanted absolutely no part of working with Gillan anymore but the record company pushed for the classic MK II lineup for the 25th anniversary album and tour and asked Blackmore how much money it would take for him to agree to allow Gillan back into the fold.  He said he named a ridiculous number knowing they would never agree to it.  But they did, much to his chagrin.  After this album, Blackmore left Deep Purple for good, never to return.  The album itself suffers from muddy production but all in all, it's a decent Deep Purple record, especially considering their time in the sun had long since passed them by.  The title track is riffy enough and Gillan conjures up some interesting self harmonized vocals.  'Lick It Up' sounds like reject 80's hair metal but 'Time to Kill' and 'Ramshackle Man' finally get Jon Lord back into the action with some cool organ licks.  'Ramshackle Man' is a pedestrian blues/boogie shuffle we've all heard a million times but this time, the soloing from Ritchie and Jon make it a worthwhile listen.  'A Twist in the Tale' features Blackmore's patented 'Spotlight Kid' riff for about the 300th time in his career while 'One Man's Meat' sounds like a throwback to Dio era Rainbow.  'Talk About Love' is a highlight.  All in all, this album, while nowhere near the ranks of classic Purple, really isn't too bad.</p>
<span class="rating"> (rating: 3/5 whammy bars)</span>
