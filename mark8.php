<h3 class='album_title_header'>=1 - <em>2020</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/cover_=_1.png" alt="Whoosh"/>
Is this the last Deep Purple studio album?  If it is, they made a pretty cool move in covering 'And the Address' to close out their career since it is the first song on their first album 'Shades of Deep Purple.'  Overall, however, this album is middle of the road.  'Whoosh' suffers from the same mistake so many artists have made since the advent of CD's;  what to do with all of that extra time available?  Vinyl was a temporally perfect format because of it's forced constraint, afterall an engineer could only cram so much material onto an LP, usually 40-45 minutes and this of course, forced all of the creative powers to make tough decisions regarding which material to include. 'Whoosh' is too long.  Ian Paice and Roger Glover prove for the umpteenth time why those two are so great together and Don Airey has definitely become the focus of the band with his amazing chops, melodic ideas, riffiness and expert soloing.  Ian Gillan? Well, his vocals ain't what they used to be but has always had a great imagination and creativity and this album is no different.  Morse is a brilliant, technical guitarist at times although he's guilty of recycling guitar solos and writing is not his strength.  Overall, this is just another average post Blackmore, post Lord album.  Nothing is truly terrible on here but nothing really stands out either.  If you're looking for a reminder of the glory days of Deep Purple, you won't find it here.  But if you're like me, and you just really love these guys and are interested in following their careers, you'll find plenty to enjoy on this record.
<span class="yellow"> (rating: 2.5/5 whammy bars)</span>
</p>

