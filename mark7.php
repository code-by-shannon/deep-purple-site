<!--Bananas-->
<h3 class='album_title_header'>Bananas<em> - 1996</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/cover_bananas.png" alt="Bananas album cover"/>
Bananas?  Between the album cover and the title it's easy to assume that this is going to be a really bad album from a band who weren't even trying anymore.  And this album does have a strangely carefree attitude about it; the lyrics are fun throughout, Morse and Airey play some terrific solos and the rhythm section is in fine form throughout.  The album starts off hot with 'House of Pain,' 'Sun Goes Down' and 'Haunted' which is an unlikely mellow, believable heartbreak ballad from Gillan.  There's a slight drop off during the middle of the album where the songs tend to get a bit generic but it still has a lot of nice moments like the cathartic 'Walk On'.  It closes out in fine fashion however.  'Bananas,' the title track, has that old school Deep Purple vibe with a wild prog extension at the end.  'Doing It Tonight' is fun and jammy and the album wraps up with a somberly coda from Steve Morse titled 'Contact Lost.'  The fun that this album exudes puts it in direct contrast with 'The House of Blue Light'.  The material between these two albums is comparable, but the spirit and execution of 'Bananas' makes it so much more enjoyable.</p>
<span class="rating"> (rating: 3.75/5 whammy bars)</span>

    
<!--Rapture of the Deep-->
<h3 class='album_title_header'>Rapture of the Deep - <em>2005</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/cover_rapture_deep.png" alt="Rapture of the Deep Cover"/>
You just never know what you're gonna get when you spin a Deep Purple platter, lineup wise, music wise and sometimes even quality wise.  'Rapture of the Deep' leads off with 'Money Talks' which is a fittingly unconventional song with bizarre Gillan harmonies and an experimental, disjointed song structure that works.  Immediately after, 'Girls Like That' is thoughtless, boring boogie rock/bad pop.  However there are a few really solid, fun, interesting tracks that elevates this disc.  'Rapture of the Deep' and 'Back to Back' along with 'Money Talks' are all solid tunes and only 'Wrong Man' is truly bad.  The rest of the songs sound better than mere filler.  Don Airey shows flashes of brilliance comparable to Lord on this album.  There are enough flashbacks to classic Deep Purple with quality guitar and organ solos, jagged rhythyms and exploratory melodies to keep the average Deep Purple fan more than interested.</p>
<span class="rating"> (rating: 3/5 whammy bars)</span>

 
<!--Now What-->
<h3 class='album_title_header'>Now What - <em>2013</em></h3>
<p class='review_text infinite_hack'>
<img class="album_cover" src="imgs/cover_now_what.png" alt="Now What Cover"/>
'Weirdistan' is the song on this album that feels like it could be a true throwback to the Deep Purple glory days while retaining a modern day feel.  It's by far the strongest song on what is otherwise, an underwhelming record.  There's little in the way of experimentation, it's short on prog, there are few if any jaw dropping solos, especially from Morse whose tendency to overplay sabotages his efforts.  Ultimately this is an album made up almost entirely of filler.
</p>
<span class="rating"> (rating: 2/5 whammy bars)</span>
    
<!--Infinite-->
<h3 class='album_title_header'>Infinite - <em>2017</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/cover_infinite.png" alt="Infinite"/>
'Infinite' is steeped in twisted up sci-fi themes and paranoid philosophies that give this incarnation of Deep Purple a familiar feel reminiscent of the classic MK II era.  Classic Mk II was notable for it's progginess and out of this world, interstellar feel, not in the dreamy, tripping through the cosmos Pink Floyd manner but more like the out of control space ship hurtling wrecklessly towards the wormhole.  On 'Infinite' the material shows excellent range, and Bob Ezrin's production is even excellent.  There are tons of great workouts from Morse and Airey on this album and most importantly, this album has an energy that is usually lacking in the Morse era Deep Purple.  'Birds of Prey' especially, is a helluva way to close out this album and Morse does some of the best playing of his life with understated, dramatic melodic movement to close this album out.  Well, there is one more song after that  . . . ahem  . . . 'Roadhouse Blues.'  Let's pretend that it never happened, better yet, turn off the record as soon as 'Birds of Prey' finishes and you got yourself a damn fine album!</p>
<span class="rating"> (rating: 4/5 whammy bars)</span>

    
<!--Whoosh-->
<h3 class='album_title_header'>Whoosh - <em>2020</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/cover_whoosh.png" alt="Whoosh"/>
Is this the last Deep Purple studio album?  If it is, they made a pretty cool move in covering 'And the Address' to close out their career since it is the first song on their first album 'Shades of Deep Purple.'  Overall, however, this album is middle of the road.  'Whoosh' suffers from the same mistake so many artists have made since the advent of CD's;  what to do with all of that extra time available?  Vinyl was a temporally perfect format because of it's forced constraint, afterall an engineer could only cram so much material onto an LP, usually 40-45 minutes and this of course, forced all of the creative powers to make tough decisions regarding which material to include. 'Whoosh' is too long.  Ian Paice and Roger Glover prove for the umpteenth time why those two are so great together and Don Airey has definitely become the focus of the band with his amazing chops, melodic ideas, riffiness and expert soloing.  Ian Gillan? Well, his vocals ain't what they used to be but has always had a great imagination and creativity and this album is no different.  Morse is a brilliant, technical guitarist at times although he's guilty of recycling guitar solos and writing is not his strength.  Overall, this is just another average post Blackmore, post Lord album.  Nothing is truly terrible on here but nothing really stands out either.  If you're looking for a reminder of the glory days of Deep Purple, you won't find it here.  But if you're like me, and you just really love these guys and are interested in following their careers, you'll find plenty to enjoy on this record.</p>
<span class="rating"> (rating: 2.5/5 whammy bars)</span>

