<!--Perfect Strangers-->
<h3 class='album_title_header'>Perfect Strangers - <em>1984</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/cover_perfect_strangers.png" alt="Perfect Strangers Cover"/>
The long awaited and anticipated reunion of not only Deep Purple but the venerated MK II lineup was not a disappointment.  It's obvious that, at least briefly, all members were excited to be back together again.  Jon Lord circles the waters like a shark on the prowl, introducing the album.  When Blackmore is enthusiastic and in good spirits, it shows in his playing and on this album, his aggressive and playful guitar work is on full display.  He doesn't let up on the throttle for rockers like 'Knocking at Your Backdoor,' or 'Gypsy's Kiss' or the eastern influenced, odd rhythm hypnosis of 'Perfect Strangers.' For good measure, Ritchie checks in with a brilliant solo on the slow burning 'Wasted Sunsets.'  Sadly, this is the last time you will hear Jon Lord really stretch out on keyboards in such an exciting manner and the duels between he and Blackmore harken back to the glory days of the MK II era in genuine fashion.  Ian Gillan, also, is in fine form, scatting and screeching with abandon, his lyrics fun and enticing and as always, nonsensical, especially on the goofy but enticing 'Gypsy's Kiss.'  This would be the last truly great Deep Purple album.  This isn't 'Machine Head', 'In Rock' or 'Burn' but instead something different than all of those but in it's own way almost as interesting.  This is also the last album by Deep Purple that that feels restless, and goads their imaginations into creating something surprising and unique.</p>

<span class="rating"> (rating: 4/5 whammy bars)</span>

<!--House of Blue Light-->
<h3 class='album_title_header'>House of Blue Light <em>- 1987</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/cover_house_blue_light.png" alt="House of Blue Light Cover"/>
This album is glossy in it's production, like Joe Lynn Turner era Rainbow.  Although the intent is to deliver accessible pop, the musicianship is, of course, exceptional, throughout.  It's quite short on progressive ideas although there are a few here and there.  The highlights are 'Bad Attitude,' and 'Mad Dog' but 'Spanish Archer' is the real standout track with it's medieval guitar opening and a relentless exciting ambience that intensifies Gillan's captivating and powerful lyrics.  It's disappointing that Jon takes a back seat throughout almost the entirety of this album, because, it just isn't classic Deep Purple without the duels between Ritchie's Stratocaster and Jon's Hammond B2.  Every Deep Purple album is better when Lord is given a chance to shine.  On the bright side, many of the tunes are riffy enough but ultimately, there are too many slow and dull moments, most heinous of all is a recycled Rainbow riff that pops up in it's 3rd or 4th iteration.  This record fails because largely because it's a product of intentionally aiming for pop success.  The result is pop that isn't good enough to be truly popular while the bands strengths are watered down when they should be showcased.
</p>
<span class="rating"> (rating: 2.5/5 whammy bars)</span> 
