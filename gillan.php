<!-- Mr. Universe -->
<h3 class='album_title_header'>Mr. Universe - <em>1979</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/i_cover_mr_universe.png" alt="Mr Universe Cover">
A new lineup and new band as Ian Gillan changes the moniker from Ian Gillan Band to just plain Gillan.  The music gets a little more simplified as well.  Gone is much of the proggy excess that constituted the first three albums and in it's place is an overt emphasis on getting back to the rockin (and hard rockin at that!) In short, a return to his purply roots.  'Mr. Universe' is more of a headbanger with its focus on riffs centered around distorted guitars.  Even the muddier production screams a change in direction.  The 5 star songs are 'Secret of the Dance' which harkens back to 'Speed King' replete with dueling guitar vs keyboard. 'She Tears Me Down' is light but rocking and 'Roller' is a heavy, progressive song with a clever ending.  Thereafter it's a steep drop off, unfortunately.  'Mr. Universe,' the song,  just can't keep the momentum and flounders in a prolonged guitar wasteland which is a shame because the keys in that one are very interesting.  'Vengeance' is a generic rocker and 'Puget' sounds like Whitesnake on a bad day.  The keys save 'Dead of Night' with a tremendous solo. 
<span>(review: 3.5/5 shrieks)</span>
</p>

