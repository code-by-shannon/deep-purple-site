<!--Accidentally On Purpose-->
<h3 class='album_title_header'>Accidentally on Purpose - <em>1988</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/i_cover_accidentally_on_purpose.png" alt = "Accidentally on Purpose Cover">
After Deep Purple finished recording 'The House of Blue Light' album, Roger and Ian took a vacation to the Caribbean and used Sir George Martin's studio to record, presumably all of the ideas that were proposed to and dismissed by Ritchie Blackmore.  Roger and Ian both were probably brimming with ideas during the Deep Purple sessions but, once again, Ian Gillan was about to find himself and ex-member at Ritchie's behest and since Roger was always willing to do whatever it took to keep things moving smoothly, he never challenged Blackmore.  Therefore, they unloaded their coffers and the result was 'Accidentally on Purpose.'  It shouldn't be a surprise that there is nothing much in the way of the Deep Purple sound here.  It isn't much of a rocker but rather the product of two capable songwriters, freed from the restrictions of our favorite musical tyrant, on vacation, worried about their future role with Deep Purple and probably, in a healthy way, not really caring.  This album doesn't have anything truly memorable on it but it's not all that bad either.  Take it for what it is, competent artistic expression from a couple of guys who could finally take a deep breath, if only for a minute.
<span>(review: 3/5 shrieks</span>
</p>

<!--Naked Thunder-->
<h3 class='album_title_header'>Naked Thunder - <em>1990</em></h3>
<p class='review_text infinite_hack'>
<img class="album_cover" src= "imgs/i_cover_naked_thunder.png" alt="Naked Thunder Cover">
Gillan found a ringer in Steve Morris, a helluva guitar player although his style is definitely of the era, his technique somewhere between Steve Vai and the hordes of Sunset Strip shredders.  A gifted guitar player no doubt, he adds a lot of excitement and spice to this album, especially on the song "Love Gun."  In spite of Morris' efforts the album is middle of the road solo work from Gillan.  Nothing overly memorable but nothing truly bad.
<span>(review: 2.5/5 shrieks)</span>
</p>

<!--Toolbox-->
<h3 class='album_title_header'>Toolbox - <em>1991</em></h3>
<p class='review_text'>
<img class="album_cover" src= "imgs/i_cover_toolbox.png" alt="Toolbox Cover">
'Toolbox' has a more updated production (for it's time) that sounds if Ian went shopping on Sunset Strip for a late 80's glam rock starter kit complete with Charvel Jackson, hairspray and spandex.  Honestly, the album starts off hot, and Steve Morris is a splendid guitar player but,  while it sounds interesting at first, it quickly wears out its welcome, becoming comical and then, finally, it's just all too much.  Kudos to Ian Gillan for trying something new, his imagination and experimentation makes him a legend, and, if it was high energy he was looking for he found it, he has even motivated himself on this album to take his yelping to a height that he would never able to recapture.  But alas, it just doesn't feel genuine.  The cool thing about classic Purple was how organic all of their tones were while riding the warp factor through outer space.  Sadly, this album blows up on launch.
<span>(review: 2.0/5 shrieks)</span>
</p>

<!-- Dreamcatcher -->
<h3 class='album_title_header'>Dreamcatcher - <em>1997</em></h3>
<p class='review_text'>
<img class="album_cover" src= "imgs/i_cover_dreamcatcher.png" alt="Dreamcatcher Cover">
There's bad news on all fronts with 'Dreamcatcher.'  If you like Ian Gillan because he's heavy and rocking you are definitely going to want to skip this one.  And if you really like music in general, likewise, you should skip it.  The opening track sets the tone with it's childish, nursery rhyme melodies and limp slide guitar work.  "You Sold My Love For a Song" follows, showing that Ian could sing jazzy numbers and sound pleasant enough, but the material he is working with is pretty bland overall and it feels more like an approach Gillan is toying with and not at all vested in.  Throughout this album Steve Morris continues shredding hair metal solos but with no regard to the soft nature of most of these songs.  His guitar solos sound out of place on this album but no matter, most of this album feels like a Lion King soundtrack gone horribly wrong.  About half way through you realize that the one or two really good songs that you've been waiting for are never going to show up.
<span>(review: 1/5 shrieks)</span>
</p>

<!--Gillan's Inn-->
<h3 class='album_title_header'>Gillan's Inn - <em>2006</em></h3>
<p class='review_text'>
<img class="album_cover" src= "imgs/i_cover_gillans_inn.png" alt="Gillan's Inn Cover">
>This double album is an updated review of Gillan's career wherein he brings in a tons of guests to revisit his old material, and, happily, a lot of very interesting names pop up on the credits including Tony Iommi, Joe Satriani, Ronnie James Dio, Uli Jon Roth and of course, quite a few Deep Purple alumni to boot.  Old songs sound fresh again with updated production and Ian Gillan is quoted as saying this was the easiest record he ever made in his life.  Although every track isn't a winner, overall, this album does flow very easily from beginning to end.  Highlights include 'Unchain Your Brain,' 'Bluesy Blue Sea,' 'Trashed,' 'No Worries,' 'No Laughing in Heaven' and 'Loving On Borrowed Time.'  Demerits for the 10000th remake of 'Smoke On The Water.'  Here are some recognizable names that grace this album:  Don Airey, Ronnie James Dio, Joe Elliot, Janick Gers, Roger Glover, Jeff Healey, Tony Iommi, Jon Lord, Steve Morris, Steve Morse, Ian Paice, Uli Jon Roth, Joe Satriani, Mickey Lee Soule.
<span>(review: 3.5/5 shrieks)</span>
</p>

<!--One Eye To Morocco-->
<h3 class='album_title_header'>One Eye to Morocco - <em>2009</em></h3>
<p class='review_text'>
<img class="album_cover" src= "imgs/i_cover_one_eye_morocco.png" alt="One Eye to Morocco Cover">
Obviously, there is more to Ian Gillan than Olympic grade shrieking and especially as he gets older he seems to favor mellower moods on his solo offerings.  After 1990, most of his solo efforts are fairly pedestrian, and this is no exception.  A man of his status always manages to dip into a pool of very talented musicians for each of his projects (what else would one expect from a singer of his pedigree?) but the material, while interesting and usually imaginative, often lacks the elusive quality that makes a song truly memorable.  This album opens with the eponymous "One Eye To Morocco" which is moody and dreamy and has a nice melody.  "Change My Ways" is a highpoint as well, with some blistering harmonica, ripping guitars and excellent keyboard work, it's a song that gains energy as it goes along.  After that, everything is mediocre at best
<span>(review: 2.5/5 shrieks)</span>
</p>

