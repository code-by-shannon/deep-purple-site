<!-- Glory Road / For Gillan Fans Only-->        
<h3 class='album_title_header'>Glory Road - <em>1980</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/i_cover_glory_road.png" alt="Glory Road Cover"/>
This album features a little more experimentation than 'Scarabus' and, as such, hearkens slightly back towards the Ian GIllan Band sound, but overall this is still a very riffy affair.  In many ways it's probably one of Gillan's more consistent albums but the rating is dragged down a notch as there aren't any songs that truly stand out.
<span>(rating: 3.5/5 shrieks)</span>
<p class='review_text'>The opening of <span class=FGFO>'For Gillan Fans Only'</span>is a bonus LP that was included with 'Glory Road'.  It's a short album that features a great deal of British, Monty Python-esque humor throughout.  A hefty dose of silliness and a sense of humor was what made Ian Gillan such a valuable addition to Deep Purple.  And for this album, the usual complicated mix of humor, prog and hard rock/avant - garde that seems to come so easily for Ian Gillan, really perks the the ears up.  This album is a little greater than the sum of it's parts.
<span>(rating: 3.75/5 shrieks)</span>
</p>


<!--Future Shock -->
<h3 class='album_title_header'>Future Shock - <em>1981</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/i_cover_future_shock.png" alt="Future Shock Cover">
Ian, as an artist, is nothing if not quirky and fun for the ears.  Even when he's bad, he's still pretty good and the great thing about Gillan is that he's never right down the middle.  On 'Future Shock' he is at his quirky solo best.  It's very nice mix of hard rock and psych.  Bernie consistently puts forth great solos that are almost enough to make you forget about Ritchie Blackmore for a minute but oops . . . when you open up the inner sleeve, there's a pic of a very young Ritchie back in his Outlaw days looking very goofy with an awkward smile.  I guess Ian just couldn't resist a stab at his old arch nemesis.  On this one 'No Laughing in Heavan,' 'Bite the Bullet,' 'If I Sang Softly' and 'For Your Dreams' make this a very strong solo effort from Ian Gillan.
<span>(review: 4/5 shrieks)</span>
</p>

<!-- Double Trouble-->
<h3 class='album_title_header'>Double Trouble - <em>1981</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/i_cover_double_trouble.png" alt="Double Trouble Cover">
The vinyl version of 'Double Trouble' is a double album with one disc dedicated to new studio material and the other a sampling of live material.  It's a hard rocking studio effort with lots of classic soloing duels between Towns and Gers who consistently play well together and elevate each other to greater heights.  And for Gillan's part, there is even more shrieking than usual.  The tones can be dated, especially with the keyboards and the production is muddy but these flaws are easy to overlook because Gillan, always the shape shifter, always restless and experimental, offers daring song attempts, sometimes even verging on New Wave.  Imagine that!  Janick Gers is a steadying presence throughout, while he and Towns play ripping solos on "I'll Rip your Spine Out." Janick has a way of turning ordinary parts on "Restless" into something more magical and interesting.  "Sunbeam" features another wicked solo from Gers, this time on the outro.  The live album has great energy throughout and is a must for Purple fans.  Some great tracks are included and they all are delivered with the swagger of a very confident band.  
<span>(review: 4/5 shrieks)</span>
</p>

