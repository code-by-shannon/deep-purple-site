<!--Clear Air Turbulence-->
<h3 class='album_title_header'>Clear Air Turbulence - <em>1977</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/i_cover_clean_air_turb.png" alt="Clean Air Turbulence Cover"/>
Another very experimental and restless album from Ian Gillan.  The Ian Gillan Band continues to lean into prog territory with the utilization of saxophones and trumpets and a generous amount of very cool keyboards sound.  The album title is derived From an aviation term describing sudden and severe turbulence in a cloudless region.  With so many stops and stutters and sudden shifts of mood and tempo, the title of this album and song of the same name is no accident.  'Five Moons' has a Weather Report feel to it.  'Money Lender' is jazzy hard rock, screamin' funk and heats up with great solos.  'Over the Hill' has high energy crescendos and maintains the funkiness found throughout the album with synths weaving in and out and abrupt changes in beat and timbre.  It's experimental and humorous breakdown includes some bizarre chanting from Ian Gillan.  'Goodhand Liza' is another off the "beaten" path foray while 'Angel Manchenio' wraps it all up with a dreamy layered intro, demented reggae and a heaping dose of funk.
<span>(rating: 3/5 shrieks)</span>
</p>

