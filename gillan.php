<!--Scarabus-->
<h3 class='album_title_header'>Scarabus - <em>1977</em></h3>
<p class='review_text'>
<img class="album_cover" src= "imgs/i_cover_scarabus.png" alt="Scarabus Cover">
This album contains more upbeat rockers, much less prog and less emphasis on keys than the first two Ian Gillan Band albums.  Overall the album has a rootsier, rock n roll kind of vibe although it is obviously still the same band with funky grooves and jittery, proggy stops and starts (i.e. 'Poor Boy Hero.')  'Mercury High' is hard rocking boogie, in fact, there is a lot more 12 bar boogie on this album than on the band's previous two offerings.  'Mad Elaine' has terrific keys and hosts a nice guitar workout from Fenwick and Towns' keyboard parts are from outer space.  Overall this album is a more accessible but less exciting mixture of boogie, funk, jazz, rock, metal and and of course the obligatory Ian Gillan screeching.  Scarabus is a nod to the Deep Purple style but it just isn't quite on par with their best.
<span>(rating: 4/5 shrieks)</span>
</p>


<!--Live At Budokon-->
<h3 class='album_title_header'>Live at Budokan - <em>1977</em></h3>
<p class='review_text'>
<img class="album_cover" src= "imgs/i_cover_live_at_budokan.png" alt="Live at Budokan Cover">
This is an excellent sampling of the live era of the Ian Gillan Band.  The Ian Gillan Band pushed and stretched the boundaries of the rock format as far as anyone could, incorporating elements of hard rock, jazz-rock, fusion and funk with Gillan's usual abstract lyrics, silly, nonsensical yet somehow usually charming lyrics.  There was never a dull moment with this band and the seemingly democratic input from musicians highly skilled at their craft makes a fan lament that this lineup was only together for a few short albums.  The most unfortunate part of this album is that there are 2 versions of 'Smoke on the Water' but this is also a testament to the talents of the band as the two versions are radically different from each other while sounding very little like the original Deep Purple version.  Likewise, interpretations of 'Child in Time' and 'My Woman From Tokyo' also pop up.  The Deep Purple covers might seem an easy and lazy choice but they actually go to show how restless and innovate Ian Gillan could be at his peak.  
<span>(rating: 5/5 shrieks)</span>
</p>

