<!--Magic-->
<h3 class='album_title_header'>Magic - <em>1982</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/i_cover_magic.png" alt="Magic Cover">
On 'Magic' the songs are either pretty darn good or pretty darn boring without much in between.  The strong songs are 'What's the Matter,' which opens the album and roars out of the gate with some highly inventive guitar work from Janick Gers followed by "Bluesy Blue Sea" which has a good balance of Ian Gillan weirdness mixed in with listenability and coherence.  "Demon Driver" is just off the rails and completely demented musically and "Living a Lie" is offbeat and mellow in an other-worldly way.  'Living for the City' is a good choice for a cover, you can never go wrong with Stevie Wonder, and Ian Gillan sings his guts out on it.
<span>(review: 3.5/5 shrieks)</span>
</p>

