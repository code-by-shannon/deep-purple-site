<p class='review_text'>
The production on this album is as sterile as an operating room and unfortunately the music is about as much fun as one.  There's not much on this album that bears a resemblance to the classic Deep Purple sound we all know and love.  At times these tracks sound more like Krokus on a bad night.  The highlight occurs on the opening track 'King of Dreams' which has a decent hook but for a pop song, the alienating middle section is prolonged and goes nowhere.  Next up is 'The Cut Runs Deep' with it's ridiculous, chanted chorus. 'Fire in the Basement' is bad, boring boogie rock.  Hearing Ritchie solo over a "follow the chords" boogie blues progression is just godawful.  Then Jon gets a turn.  You can hear just as good or better at your local open mic (I mean no disrespect to local musicians).  From here, the album continues to muddle around in sub mediocrity.  It's worse than bad.  It's boring. 1 star is too generous</p>
