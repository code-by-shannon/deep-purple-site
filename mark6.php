<!--Purpendicular-->
<h3 class='album_title_header'>Purpendicular - <em>1996</em></h3> 
<p class='review_text'>
<img class="album_cover" src="imgs/cover_purpendicular.png" alt="Purpendicular Cover"/>
From this point forward, Deep Purple (which always seemed like Ritchie's band) will feel much like an Ian Gillan solo project. 'Purpendicular,' doesn't launch the Steve Morse era off to a very promising start.  A gifted, technical guitar player, his style is a departure from Ritchie, and with his signature artificial harmonics and ultra clean, ultra fast speed bursts it's obvious that this is never going to be the classic Deep Purple sound.  With Ritchie, even when the albums were sub par you could put a great solo on each song in the bank.  But after the 'Battle Rages On', it was obvious that Ritchie's genius no longer outweighed his petulance.  Sadly, it wouldn't be the same band without him.  There's no need to disparage the Morse era, however, as it is further evidence that Deep Purple is never the same band for long and the different lineups have taught us that the only musical style that is constant with this hard rock band, is that nothing will ever be constant.  The album starts off great with 'Ted the Mechanic' and 'Loosen My Strings', the first raucous and rocking and the second a cool, bluesy and soulful number with an interesting ascending chord progression.  There are nice moments sprinkled throughout and the musical range is evident when you consider the bona fide celtic reading of 'Cascades' but overall the songs sound generic and boring.  A sign of life finally pops up towards the end with 'Somebody Stole My Guitar' but ultimately, clocking in at close to 60 minutes this album suffers from the curse of the CD era, it's just way too long.  Editing the material down to say, 35 minutes or so might have made this album flow a lot better and helped it's rating.
<span class="yellow"> (rating: 2/5 whammy bars)</span>
</p>

<!--Abandon-->
<h3 class='album_title_header'>Abandon - <em>1998</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/cover_abandon.png" alt="Abandon Cover"/>
Steve Morse's style is more reminiscent of Gillan's old hot shot guitar player Janick Gers than Blackmore's which strengthens the comparison between Morse era Deep Purple and Ian Gillan's solo work. That's not all bad, Gers was a shredder of epic proportions.  'Abandon' is a diverse enough album, stylistically, with a few good ballads mixed in with heavier sounding tracks.  A bit of prog is sprinkled in as well, with uneven, but interesting results and a handful of truly great moments along the way.
<span class="yellow"> (rating: 3/5 whammy bars)</span>
</p>

