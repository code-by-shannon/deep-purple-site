<!-- Bent Out of Shape--> 
<h3 class='album_title_header'>Bent Out of Shape - <em>1983</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/r_cover_bent_out_of_shape.png" alt="Bent Out Of Shape"/>
Poppy?  Yes.  Foreigner clones?  At best.  Survivor clones?  At worst.  It's still hard rock but the pop and gloss sheen just doesn't hold up for a full album.  Joe Lynn Turner tries to will some life into these songs and there are a handful of great Blackmore solos as usual but it's just not enough to be memorable pop and it's not hard enough or adventurous enough to be good hard rock.  It loses on both fronts.  The album starts promising with 'Stranded' and 'Can't Let You Go' and then much later in the album they hit a home run with 'Street of Dreams.'  The rest is entirely forgettable.  Skip this one, all the songs you need from here are on the essential Finyl Vinyl.</p> 
<span class='rating'>(rating: 1.5/5 whammy bars)</span>

