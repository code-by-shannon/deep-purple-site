<!--Down to Earth-->
<h3 class='album_title_header'>Down To Earth - <em>1979</em></h3> 
<p class='review_text'>
<img class="album_cover" src="imgs/r_cover_down_to_earth.png" alt="Down To Earth"/>
New lineup, new direction which is to say, same Blackmore.  Lyrically we have exited the realm of Dio's swords and dragons.  Mostly the album will focus on Bonnet's lyrics about sex and love with Glover chipping in the lyrics from 'Eyes of the World' and 'Lost in Hollywood.'  'All Night Long' pumps along due to Cozy's fantastic drumming and this song is poppy in a good, gritty way that few other bands can approach.  'Eyes of the World' sounds like Dio era Rainbow and Graham delivers but it's the killer guitar work that really makes this song stand out.  'Makin Love' has a beautiful harmonized guitar solo.  'Since You've Been Gone' is a legendary song with Hall of Fame worthy guitar solos.  'Love's No Friend' seems like another stab at 'Mistreated' but comes up well short.  'Danger Zone' has a very cool breakdown in the middle with a fantastic Blackmore vs Airey dual, evoking the sounds of classic Deep Purple.  Overall, this is an underrated and underappreciated Rainbow album, albeit with a couple of slow spots. </p> 
<span class='rating'>(rating: 4/5 whammy bars)</span>


