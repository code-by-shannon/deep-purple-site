<!--Rising-->
<h3 class='album_title_header'>Rising - <em>1976</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/r_cover_rising.png" alt="Rising Cover"/>
'Rising' is the first of two perfect albums in a row for Rainbow.  Every song on this lp is brilliant.  New drummer Cozy Powell thunders away on the kit, Ritchie returns to a creative highpoint not seen since 'Burn,' and Ronnie James Dio esablishes himself as a metal goliath.   Tony Carey (keyboards) fires the opening salvo and then has a number of jaw dropping solos throughout. Jimmy Bain is a world class bassist whose track record speaks for itself.  This is a classic, loaded lineup stuffed with hall of fame talent and they produce a flawless record!  It's a pity that Ritchie refused to keep this lineup around for more than one album.  This band was every bit as talented and exciting as classic Deep Purple.</p> 
<span class='rating'>(rating: 5/5 whammy bars)</span>


