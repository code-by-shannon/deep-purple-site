<!-- Long Live Rock and Roll -->
<h3 class='album_title_header'>Long Live Rock n Roll - <em>1977</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/r_cover_long_live_rr.png" alt="Long Live Rock and Roll Cover"/>
Is this the best of the Rainbow studio albums?  That's arguable but this one definitely finds Dio, Blackmore and Powell at their artistic peaks.  The title track is high octane while 'Lady of the Lake' slithers along with hypnotizing slide work from Ritchie.  'LA Connection' has a swirling opening riff and plodding bass that captures a long, exhausting trek to the promised land of fool's gold.  'Gates of Babylon' is possibly the best guitar solo of Ritchie's career, perhaps tied with the solo from 'Child In Time' on 'In Rock' and this is from an exceptional lead guitarist who, let's face it, had a whole helluva a lot of terrific solos to choose from.  'Gates Of Babylon' ends with a very beautiful solo violin meandering and closing out the first side.  Side two opens with Cozy pioneering a proto speed-metal rhythm pulse that pushes 'Kill the King' to a whole new genre.  Blackmore fires his Strat like a kitted out machine gun.  In the middle, we have a nice jiggy middle 16 bar section that obviously caught Iron Maiden's ears.  The album concludes with the gorgeous, slightly out of context but nevertheless brilliant 'Rainbow Eyes' featuring Ronnie's wonderful voice, and tasteful and restrained guitar from Ritchie. There's not a dull moment on this album.  Essential!</p> 
<span class='rating'>(rating: 5/5 whammy bars)</span>


