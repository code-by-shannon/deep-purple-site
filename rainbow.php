<!--Difficult to Cure-->
<h3 class='album_title_header'>Difficult to Cure - <em>1981</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/r_cover_difficult_to_cure.png" alt="Difficult to Cure Cover"/>
At Ritchies's whim, Rainbow completes its transition from a dragon slaying metal band to an FM radio pop rock AOR format. A few songs actually manage to pull it off, but once again, the one's you really need, "I Surrender," "Spotlight Kid," "Can't Happen Here," and "Difficult To Cure" can be found on Finyl Vinyl, the essential Rainbow compilation album.  The rest of the songs on this album that were not mentioned, drag this album down.  A fun part of the album is being pranked by Ritchie with a locking track at the end of the record.</p>
<span class='rating'>(rating: 3/5 whammy bars)</span>


