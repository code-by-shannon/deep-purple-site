<!--Stranger In Us All-->
<h3 class='album_title_header'>Stranger In Us All - <em>1995</em></h3>
<p class='review_text'>
<img class="album_cover" src="imgs/r_cover_stranger_in_us_all.png" alt="Stranger In Us All"/>
And herein lies the last time Ritchie will rock.  His guitar playing is solid if not spectacular, that is a given no matter which recording or which era.  Likewise, his lineup will always consist of new members.  For 'Stranger In Us All' he starts up a whole new band, but even with all the fresh blood and the endless magic his guitar seems capabale of producing, it's not enough to rescure this album from being at best, very average.  There are a few exciting moments; the songs "Hunting Humans," "Black Masquerade" and "Silence" aren't legendary songs, but they are fun to listen to.  But every other track is a big drop off.  Doogie White is a good singer but he just doesn't quite measure up to the amazing vocalists that we're used to hearing Ritchie play with.  The same goes for the rest of the band: servicable, quality musicians, but no real magic.  As usual, the reason for a subpar outing from Ritchie is due to him having had one foot out the door.  This time his heart was set on working with his soon to be wife Candace and starting up their midievel folk career, which he would continue with until the current day.  Apart from a very short lived Rainbow tour in 2018, Ritchie's rock output was finished.  'Stranger In Us All' makes one wonder if, perhaps, that was for the best.</p> 
<span class='rating'>(rating: 2.5/5 whammy bars)</span>


